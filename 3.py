A = [-2, 2]
B = [-2, 5]
C = [2, 5]

AB = int((A[0] - B[0])** 2 + (A[1] - B[1])** 2)
AC = ((A[0] - C[0])** 2 + (A[1] - C[1])** 2)
BC = ((B[0] - C[0])** 2 + (B[1] - C[1])** 2)

if (AB + AC) == BC \
        or (AB + BC) == AC \
        or (AC + BC) == AB :
    print("Прямоугольный треугольник")
else: print("Другой треугольник")
